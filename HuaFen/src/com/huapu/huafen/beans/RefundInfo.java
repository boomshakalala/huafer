package com.huapu.huafen.beans;

public class RefundInfo extends BaseResult{
	private long refundId;
	private String refundLabel;
	private String refundContent;
	private long refundLabelId;
	private int refundStatus;
	private long refundTime;
	private int refundMoney;
	private int refundType;
	public long getRefundId() {
		return refundId;
	}
	public void setRefundId(long refundId) {
		this.refundId = refundId;
	}
	public String getRefundLabel() {
		return refundLabel;
	}
	public void setRefundLabel(String refundLabel) {
		this.refundLabel = refundLabel;
	}
	public String getRefundContent() {
		return refundContent;
	}
	public void setRefundContent(String refundContent) {
		this.refundContent = refundContent;
	}
	public long getRefundLabelId() {
		return refundLabelId;
	}
	public void setRefundLabelId(long refundLabelId) {
		this.refundLabelId = refundLabelId;
	}
	public int getRefundStatus() {
		return refundStatus;
	}
	public void setRefundStatus(int refundStatus) {
		this.refundStatus = refundStatus;
	}
	public long getRefundTime() {
		return refundTime;
	}
	public void setRefundTime(long refundTime) {
		this.refundTime = refundTime;
	}
	public int getRefundMoney() {
		return refundMoney;
	}
	public void setRefundMoney(int refundMoney) {
		this.refundMoney = refundMoney;
	}
	public int getRefundType() {
		return refundType;
	}
	public void setRefundType(int refundType) {
		this.refundType = refundType;
	}
	
	
	
	
}
